/// Defines different motion patterns for wave animations.
///
/// Each pattern creates a unique visual effect by controlling
/// how the waves move and transform over time.
enum WaveMotionPattern {
  /// Simple horizontal wave motion from left to right.
  classic,

  /// Dynamic flow-like motion with varying speeds across wave lines.
  flowField,

  /// Smooth drifting motion resembling ribbons flowing in the wind.
  ribbonDrift,

  /// Flow field pattern with seamless looping animation.
  flowFieldLoop,

  /// Ribbon drift pattern with continuous looping effect.
  ribbonLoop,
}
